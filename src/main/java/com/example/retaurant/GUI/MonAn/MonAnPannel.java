    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // TODO add your handling code here:
        String[] columnNames = {"STT", "ID Món", "Tên Món", "Giá thành"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        int stt = 1;
        jTable1.setModel(model);
        model.setRowCount(0);
        MonAnBUS monAnBUS = new MonAnBUS();
        List<MonAnDTO> dsMonAn = monAnBUS.searchMonAnByName(jTextField1.getText());
        for (MonAnDTO monAn : dsMonAn) {
            model.addRow(new Object[]{stt,monAn.getSpId(), monAn.getTenSp(), monAn.getGiaSp()});
            stt++;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một món ăn để xoá.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá món ăn này?", "Xác nhận xoá", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        int maMon = (int) jTable1.getValueAt(selectedRow, 1);
        MonAnBUS monAnBUS= new MonAnBUS();
        if (monAnBUS.deleteMonAn(maMon)) {
            JOptionPane.showMessageDialog(this, "Xoá món ăn thành công.");
            loadTableData();
        } else {
            JOptionPane.showMessageDialog(this, "Xoá món ăn thất bại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

