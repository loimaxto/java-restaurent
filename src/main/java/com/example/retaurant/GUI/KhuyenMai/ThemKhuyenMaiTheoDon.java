    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int id_km = Integer.parseInt(txtId.getText());
        String ten_km = txtTen.getText();
        Date ngayBD = null;
        Date ngayKT = null;
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
        try {
            ngayBD = sdf.parse(txtNgayBD.getText());
        } catch (ParseException ex) {
            Logger.getLogger(ThemKhuyenMaiTheoDon.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            ngayKT = sdf.parse(txtNgayKT.getText());
            
        } catch (ParseException ex) {
            Logger.getLogger(ThemKhuyenMaiTheoDon.class.getName()).log(Level.SEVERE, null, ex);
        }
        java.sql.Date sqlNgayBD = new java.sql.Date(ngayBD.getTime());
        java.sql.Date sqlNgayKT = new java.sql.Date(ngayKT.getTime());

        int phan_tram = Integer.parseInt(txtPhanTram.getText());
        KhuyenMaiDTO km = new KhuyenMaiDTO(id_km, ten_km, sqlNgayBD, sqlNgayKT, 1);
        KhuyenMaiTheoDonDTO kmTheoDon = new KhuyenMaiTheoDonDTO(id_km,phan_tram);
        
        KhuyenMaiBUS khuyenMaiBUS = new KhuyenMaiBUS();
        khuyenMaiBUS.addKhuyenMai(km);
        KhuyenMaiTheoDonBUS khuyenMaiTheoDonBUS = new KhuyenMaiTheoDonBUS();
        khuyenMaiTheoDonBUS.addKhuyenMaiTheoDon(kmTheoDon);
        
        
    }//GEN-LAST:event_btnThemActionPerformed

