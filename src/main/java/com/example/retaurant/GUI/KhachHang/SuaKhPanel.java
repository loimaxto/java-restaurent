        // TODO add your handling code here:
        CustomerDTO newCust = new CustomerDTO();
        newCust.setHoKh(textFieldHo.getText());
        newCust.setTenKh(textFieldTen.getText());
        newCust.setSdt(textFieldSdt.getText());
        newCust.setKhId(currentCust.getKhId());
        boolean isSuccessUpdate = busCustomer.updateCustomer(newCust);
        
        if ( khPanel != null) {
            khPanel.loadDataTable();
        } 
        if ( isSuccessUpdate ){
            MyDialog dialog = new MyDialog("Cập nhật thành công", 0);
            closePanel(evt);
        } 
